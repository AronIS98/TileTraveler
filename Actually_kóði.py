
def step(position,command):
    '''Moves position in a 3x3 grid'''
    if command == 'n':
        position += 0.1
    elif command == 's':
        position -= 0.1
    elif command == 'e':
        position += 1.0
    elif command == 'w':
        position -= 1.0
    return position


